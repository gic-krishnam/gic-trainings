package day2grid;

public class program {
	    public static void main(String[] args) {
	        
	        int i = 0;
	        int z = 10;
	        
	        // Loop with two variables.
	        while (i < z) {
	            i++;
	            z--;
	            
	            // Display the values.
	            System.out.println(i + "/" + z);
	        }
	    }
	}
